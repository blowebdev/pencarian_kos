            #Tentang Aplikasi
        </div>
        <div class="panel-body">
            KosFinder adalah sebuah platform pencarian kos online yang menyediakan fitur unggulan berupa jarak rute terdekat. Dengan memanfaatkan teknologi pemetaan dan algoritma pencarian terbaru, KosFinder membantu pengguna dalam mencari dan memilih kos yang berada dalam jarak terdekat dengan tujuan atau lokasi yang diinginkan. Tujuan utama KosFinder adalah memberikan pengalaman pengguna yang efisien, cepat, dan nyaman dalam mencari kos.
            <br>
            <br>
            Fitur Utama:  <br>

            <b>Pencarian Kos:</b>  <br>
            Pengguna dapat memasukkan kriteria pencarian seperti lokasi, harga, fasilitas, dan tipe kos yang diinginkan. KosFinder akan memberikan hasil pencarian yang relevan sesuai dengan kriteria yang dimasukkan pengguna.<br>

            <b>Pemetaan Interaktif:</b>  <br>
            KosFinder dilengkapi dengan peta interaktif yang memungkinkan pengguna untuk melihat lokasi kos dan tujuan dengan jelas. Peta ini memudahkan pengguna dalam memahami jarak dan rute terdekat dari kos yang mereka minati.<br>

            <b>Rute Terdekat:</b>  <br>
            Fitur utama KosFinder adalah kemampuannya untuk menampilkan jarak dan rute terdekat antara kos yang dipilih oleh pengguna dan tujuan yang ditentukan. Pengguna dapat melihat rute terbaik yang harus diikuti untuk mencapai tujuan mereka dengan cepat dan efisien.<br>

            <b>Informasi Detail Kos:</b>  <br>
            KosFinder memberikan informasi yang lengkap tentang kos yang terdaftar, termasuk deskripsi, fasilitas, harga, gambar, dan ulasan dari penghuni sebelumnya. Hal ini membantu pengguna untuk membuat keputusan yang tepat sebelum memilih kos yang sesuai dengan kebutuhan dan preferensi mereka.<br>

            <b>Perbandingan Kos:</b>  <br>
            KosFinder juga menyediakan fitur perbandingan kos yang memungkinkan pengguna untuk membandingkan beberapa kos sekaligus berdasarkan kriteria yang mereka pilih. Pengguna dapat melihat perbedaan harga, fasilitas, dan jarak terhadap tujuan yang mereka inginkan.<br>

            <b>Filter Lanjutan:</b>  <br>
            Platform ini dilengkapi dengan fitur filter lanjutan yang memungkinkan pengguna untuk menyaring hasil pencarian berdasarkan preferensi khusus, seperti jarak maksimum yang dapat diterima, fasilitas tambahan, kebijakan kos, dan lain-lain.<br>

            <b>Ulasan Pengguna:</b>  <br>
            KosFinder memungkinkan pengguna untuk memberikan ulasan dan penilaian terhadap kos yang mereka tinggali sebelumnya. Hal ini membantu calon penghuni lainnya untuk mendapatkan wawasan yang lebih baik tentang kualitas dan kecocokan kos yang mereka minati.
            <br>
            <b>Kesimpulan:</b>  <br>
            Dengan fitur-fitur unggulan seperti jarak rute terdekat, KosFinder menjadi solusi yang efektif bagi mereka yang mencari kos dengan kemudahan akses ke tujuan yang diinginkan. Dengan platform ini, pengguna dapat menghemat waktu dan usaha dalam menemukan kos yang sesuai dengan preferensi mereka. KosFinder memastikan pengalaman pengguna yang efisien, cepat, dan nyaman dalam pencarian<br>
